# Tipos básicos em Python:
# str  -> Representa textos (strings). Sempre entre aspas, ex: "Olá", "123".
# int  -> Representa números inteiros, positivos ou negativos, ex: 10, -3, 0.
# float -> Representa números decimais (ponto flutuante), ex: 3.14, 0.5.
# bool -> Representa valores lógicos: True ou False. Usado em condições.
print(type(11))
print(type(1.1))
print(type("11"))
print(type(True))

